# FUNCTIONS USED FOR PERFORMING CALCULATIONS

def add(x: float, y: float) -> float:
    """Takes in two numbers (x and y), returns the result from adding the two numbers"""
    return x + y


def subtract(x: float, y: float) -> float:
    """Takes in two numbers (x and y), returns the result from subtracting y from x"""
    return x - y


def multiply(x: float, y: float) -> float:
    """Takes in two numbers (x and y), returns the result from multiplying the two numbers"""
    return x * y


def divide(x: float, y: float) -> float:
    """Takes in two numbers (x and y), returns the result from dividing x by y"""
    return x / y
